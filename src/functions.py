    for x in range(len(dfs)-1):

        df = df.append(dfs[x])

    df.columns = [x.lower() for x in df.columns]
    df.name = [x.title() for x in df.name]
    df = df.drop_duplicates()

    df = df.sort_values(by='name')

    return df

